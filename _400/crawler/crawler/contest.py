"""
- String Truncate:
  Truncates a variable to a character length. You can specify the length with the limit argument.
  The delimiter and extension arguments are optional.
  The delimiter can be any character, like word boundary.
  You can specify a string of text to display at the end if the variable was truncated.
  example:
  >>> print truncate("This is an example of long string.", 15, " ", "...")
  # This is an example...
"""
def truncate(string, limit=0, delimiter="", extension=""):
	if limit > 0:
		if len(string) > limit:
			pos, left, right = 0, 0, 0
			if string.find(delimiter, limit) != -1:
				right = string.find(delimiter, limit)
			temp = 0
			while temp <= limit:
				if string[temp] == delimiter:
					left = temp
				temp += 1
			if limit-left < right-limit:
				pos = left
			else:
				pos = right
			if pos:
				return string[:pos] + extension
			else:
				return string[:limit] + extension
	return string
			

"""
- Url Truncate:
  Truncates a url to a character length. You can specify the length with the limit argument.
  You can use this procedure for showing search results.
  The extension arguments are optional. You can specify a string of text to display at the end if the variable was truncated.
  example:
  >>> print url_truncate("http://livebook.in/2012/04/using-python-on-windows-along-notepad/", 20, "...")
  # livebook.in/2012/04/...
"""
def url_truncate(url, limit=0, extension=""):
	if limit > 0:
		import re
		p = re.compile(r'[a-z0-9]{3,6}://')
		url = p.sub('', url, re.I)
		if url:
			if url[-1] == "/":
				url = url[:-1]
		url = truncate(url, limit, "/", extension)
	return url


