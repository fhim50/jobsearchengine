"""
- Base url:
  returns a url up to the 3rd slash, without the trailing slash.
  example:
  >>> print base_url("http://www.mysite.com/index.html")
  # http://www.mysite.com
"""
def base_url(url):
	if url.find("/") != -1:
		pos = 0;
		for i in range(0,3):
			pos = url.find("/", pos)
			pos += 1
		if pos == 0:
			return url
		else:
			return url[0:pos-1]
	else:
		return ""
	
		
"""
- Raw url:
  return only the domain name.
  example:
  >>> print raw_url("http://www.mysite.com/index.html")
  # mysite.com
"""
def raw_url(url):
	if url.find("/") != -1:
		url = base_url(url)
		pos = 0
		for i in range(0,2):
			pos = url.find("/", pos)
			pos += 1
		url = url[pos:]
		if url.find("www.") != -1:
			url = url.replace("www.", "")
		return url
	else:
		return ""


"""
- Virus and Malware checking procedure:
  Based on the open source ClamAV engine.
  Returns True if the file is infected or False if not.
  example:
  >>> print scan_this("http://www.eicar.org/download/eicar_com.zip")
  # True
  Note that it takes a few seconds to scan the given url
"""		
def scan_this(url):
	rep = 'http://scanthis.net/api'
	data = {}
	data["action"] = "download"
	data["url"] = url
	data["clientid"] = crawler_name
	data["clientversion"] = crawler_ver
	res = post_data(rep, data)
	res = json_decode(res)
	if res["success"] == True:
		def check_result(id):
			data["action"] = "check"
			data["id"] = id
			res = post_data(rep, data)
			res = json_decode(res)
			return res
		while True:
			chk = check_result(res["id"])
			if not chk["scanned"]:
				chk = check_result(res["id"])
			else:
				break	
		return chk["infected"]
	else:
		scan_this(url)

		
"""
- Phishtank Antiphishing:
  Check sites againts Phishtank antiphishing database. (http://www.phishtank.com/)
  This procedure downloads the latest database itself and updates it, if it's older than 24 hours.
  Note:  make sure python has sufficent permission for creating and modifying files on the current working directory (777)
  The output of this procedure is boolean. if the given url is in the fishing sites database, it returns True.
  If it's not, it returns false.
  example:
  >>> print phishtank("http://paypal.centrodeatendimento.com.br/paypal/index.htm")
  # True
"""
def phishtank(url):
	import os,sys,time
	dl_addr = "http://data.phishtank.com/data/online-valid.json.gz"
	dl_fname = "online-valid.json.gz"
	current_db = sys.path[0] + os.sep + dl_fname
	if os.path.exists(current_db):
		if (time.time() - os.path.getmtime(current_db)) > (24*60*60):
			download(dl_addr, current_db)
	else:
		download(dl_addr, current_db)
	database = gz_read(current_db)
	database = json_decode(database)
	for e in database:
		if raw_url(url) == raw_url(e["url"]):
			return True
	return False
	

